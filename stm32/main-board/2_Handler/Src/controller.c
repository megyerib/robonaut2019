//! @param tf
//! @param an
//! @return
//!
//! @example
double controllerTransferFunction (cFirstOrderTF* tf, double an)
{
	double bn;

	bn = tf->b0 * an + tf->b1 * tf->an_past - tf->a1 * tf->bn_past;

	tf->bn_past = bn;
	tf->an_past = an;

	return bn;
}

